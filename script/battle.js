// проверка правильности ответа
function сheckQuestion (check) {
  if (check == arrayCurrentQuestion[currentQuestion].correct) {
    countRightAnswer ++;
    writeStatistics (arrayCurrentQuestion[currentQuestion].tag, 'right');
    animationAnswerRight (check);
    hit ("hitEnemy");
    damageNumber (currentHero.damageMin, currentHero.damageMax);
    drawHit ('damageEnemy');
    setTimeout(() => healthEnemy -= damage, time);
    deleteQuestion ();
  } else {
    countWrongAnswer ++;
    writeStatistics (arrayCurrentQuestion[currentQuestion].tag, 'wrong');
    animationAnswerWrong (check);
    hit ("hitHero");
    damageNumber (currentEnemy.damageMin, currentEnemy.damageMax);
    drawHit ('damageHero');
    setTimeout(() => healthHero -= damage, time);
    deleteQuestion ();
  }
}

