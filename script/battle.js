//проверка уровня
function calcExp (result) {
  percentResult = Math.round((countRightAnswer/(countRightAnswer+countWrongAnswer)) * 100);
  if (result == "Победа" && percentResult > 0 && percentResult <= 20) {
    currentStars = 1;
    endTitle = 'Почти';
  }
  if (result == "Победа" && percentResult > 20 && percentResult <= 40) {
    currentStars = 2;
    endTitle = 'Еще немного практики';
  }
  if (result == "Победа" && percentResult > 40 && percentResult <= 60) {
    currentStars = 3;
    endTitle = 'Хорошо';
  }
  if (result == "Победа" && percentResult > 60 && percentResult <= 80) {
    currentStars = 4;
    endTitle = 'Отлично';
  }
  if (result == "Победа" && percentResult > 80 && percentResult <= 99) {
    currentStars = 5;
    endTitle = 'Превосходно';
  }
  if (result == "Победа" && percentResult == 100) {
    localStorage.setItem ('achievementCompleteSaved', "enable");
    currentStars = 5;
    endTitle = 'Чистая победа';
  }
  if (result == "Поражение") {
    currentStars = 0;
    endTitle = 'Нужно больше практики';
  }
  stars = +stars + currentStars;
  localStorage.setItem('starsSaved', stars);
}
