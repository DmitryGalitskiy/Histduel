// создание массива с выбранными вопросами
function createTagArray () {
  for (let x = 0; x < switchResultTag.length; x++) {
    for (let i = 0; i < arrayAllQuestion.length; i++) {
      let selectedQuestion = (arrayAllQuestion.find ((item, index) =>
      item.tag == switchResultTag[x] && index == i));
      if (selectedQuestion !== undefined) {
        arrayCurrentQuestion.push(selectedQuestion);
      }
    }
  }
}
